def translate_tag_with_dict(tag: str) -> str:
    """Try to translate a tag using the static dictionary. Returns None if not found."""
    # Normalize tag (lowercase check) for better matching
    tag_clean = tag.strip()
    
    # Direct match
    if tag_clean in TAG_DICTIONARY:
        return TAG_DICTIONARY[tag_clean]
        
    # Case-insensitive match
    for key, value in TAG_DICTIONARY.items():
        if key.lower() == tag_clean.lower():
            return value
            
    return None
